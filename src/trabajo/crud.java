    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        try {
            conexion = (Connection) ConexioDB.getConnection();
            PreparedStatement consulta = null;
            String query = "INSERT INTO anime VALUES(?,?,?,?,?)";
            consulta = conexion.prepareStatement(query);
            consulta.setString(1, nombre.getText());
            consulta.setString(2, Estudio.getText());
            consulta.setInt(3, Integer.parseInt(año.getText()));
            consulta.setString(4, Director.getText());
            consulta.setInt(5, Integer.parseInt(Episodios.getText()));
            consulta.execute();
            borrar();
            nombre.setEnabled(false);
            año.setEnabled(false);
            Director.setEnabled(false);
            Episodios.setEnabled(false);
            Estudio.setEnabled(false);
            guardar.setEnabled(false);
            eliminar.setEnabled(false);
            Refrescar.setEnabled(true);
            consulta.close();

        } catch (SQLException ex) {
            Logger.getLogger(crud.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_guardarActionPerformed

