    private void mostrar() {
        DefaultTableModel modelo = new DefaultTableModel();

        ResultSet rs = ConexioDB.getTabla("select * from anime");
        modelo.setColumnIdentifiers(new Object[]{"Nombres", "Estudio", "Año", "Director", "Episodios"});

        try {
            while (rs.next()) {
                // añade los resultado a al modelo de tabla

                modelo.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getInt(3), rs.getString(4), rs.getInt(5)});
            }
            table.setModel(modelo);

        } catch (Exception e) {
            System.out.println(e);
        }

    }

